namespace HumanTimeParser.Core.TimeConstructs
{
    /// <summary>
    /// Represents a time period used in the twelve hour clock system.
    /// </summary>
    public enum TimePeriod
    {
        /// <summary>
        /// Represents a time before noon and after midnight.
        /// </summary>
        Am,
        /// <summary>
        /// Represents a time after noon and before midnight.
        /// </summary>
        Pm
    }
}